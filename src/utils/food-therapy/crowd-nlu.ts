// 合并：基础词库 + 扩充词库（扩充项可覆盖同键，但此处仅新增键）
export const CROWD_KEYWORD_MAP: Record<string, Crowd[]> = { ...CROWD_KEYWORD_MAP_BASE, ...CROWD_KEYWORD_MAP_EXTRA }

/**
 * 从用户输入文本中识别身体状态人群（食养参考维度）。
 * 子串匹配，长词优先；返回去重后的 Crowd[]（无序）。
 * 未命中任何关键词返回空数组。
 */
export function parseCrowdsFromText(input: string): Crowd[] {
  if (!input || !input.trim()) return []
  const found = new Set<Crowd>()
  // 长关键词优先匹配，避免"好"先命中而漏掉"好累"类长词
  const keywords = Object.keys(CROWD_KEYWORD_MAP).sort((a, b) => b.length - a.length)
  for (const kw of keywords) {
    if (input.includes(kw)) {
      for (const c of CROWD_KEYWORD_MAP[kw]) found.add(c)
    }
  }
  return Array.from(found)
}

// 便于调用方判断某人群是否被识别（防止误配非 CROWD_OPTIONS 取值）
export function isKnownCrowd(value: string): value is Crowd {
  return (CROWD_OPTIONS as readonly string[]).includes(value)
}

